typedef struct {
	unsigned char state[256];
	unsigned char x, y;
} rc4_key;

void rc4_init(rc4_key* key, unsigned char* data, int data_len)
{
	int i, j = 0;
	unsigned char swap;
	for (i = 0; i < 256; i++)
		key->state[i] = i;
	for (i = 0; i < 256; i++)
	{
		j = (j + key->state[i] + data[i % data_len]) % 256;
		swap = key->state[i];
		key->state[i] = key->state[j];
		key->state[j] = swap;
	}
	key->x = key->y = 0;
}

void rc4_crypt(rc4_key* key, unsigned char* data, int data_len)
{
	int i;
	unsigned char x, y, swap;
	x = key->x;
	y = key->y;
	for (i = 0; i < data_len; i++)
	{
		x = (x + 1) % 256;
		y = (y + key->state[x]) % 256;
		swap = key->state[x];
		key->state[x] = key->state[y];
		key->state[y] = swap;
		data[i] ^= key->state[(key->state[x] + key->state[y]) % 256];
	}
	key->x = x;
	key->y = y;
}


